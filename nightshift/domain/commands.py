"""UART command constants and helpers for T5-Link v1."""

from __future__ import annotations

# System commands
HELLO = 0x0001
HEARTBEAT = 0x0002
GET_INFO = 0x0003
TIME_SYNC = 0x0004
STATE_SYNC_BEGIN = 0x0010
STATE_SYNC_END = 0x0011

# State commands
MODE_SET = 0x1001
ATTENTION_SET = 0x1002
WORK_STATE_SET = 0x1003
DASHBOARD_SET = 0x1004
NOTICE_SHOW = 0x1005

# Task list commands
TASK_LIST_BEGIN = 0x1101
TASK_ITEM = 0x1102
TASK_LIST_END = 0x1103

# User actions
UI_ACTION = 0x2001
PAGE_EVENT = 0x2002
TOUCH_EVENT_DEBUG = 0x2003

# LED and backlight
LED_OVERRIDE = 0x3001
BACKLIGHT_SET = 0x3002

# Audio (reserved for future phases)
AUDIO_PLAY = 0x4001
AUDIO_STOP = 0x4002
VOLUME_SET = 0x4003
AUDIO_EVENT = 0x4004
MIC_START = 0x4010
MIC_STOP = 0x4011
MIC_EVENT = 0x4012

# Status codes
OK = 0x0000
ACCEPTED = 0x0001
UNKNOWN_COMMAND = 0x0002
INVALID_LENGTH = 0x0003
INVALID_ARGUMENT = 0x0004
BUSY = 0x0005
NOT_READY = 0x0006
NOT_FOUND = 0x0007
INTERNAL_ERROR = 0x0008
UNSUPPORTED_VERSION = 0x0009
NO_SPACE = 0x000A
STATE_CONFLICT = 0x000B

# Mode values
MODE_IDLE = 0
MODE_DAY_WORK = 1
MODE_NIGHT_EXEC = 2

# Work state values
WORK_STOPPED = 0
WORK_STARTING = 1
WORK_RUNNING = 2
WORK_PAUSED = 3
WORK_COMPLETED = 4
WORK_FAILED = 5

# UI action values
ACTION_CONFIRM = 1
ACTION_REJECT = 2
ACTION_RETRY = 3
ACTION_PAUSE_EXECUTION = 4
ACTION_RESUME_EXECUTION = 5
ACTION_OPEN_TASK = 6
ACTION_CLOSE_TASK = 7
ACTION_START_VOICE = 8
ACTION_STOP_VOICE = 9
ACTION_NEXT_PAGE = 10
ACTION_PREVIOUS_PAGE = 11
ACTION_DISMISS_NOTICE = 12

# Flags
FLAG_ACK_REQ = 1 << 0
FLAG_RESPONSE = 1 << 1
FLAG_EVENT = 1 << 2
FLAG_MORE = 1 << 3
FLAG_HIGH_PRIORITY = 1 << 4

# Capabilities
CAP_LCD = 1 << 0
CAP_TOUCH = 1 << 1
CAP_LED = 1 << 2
CAP_SPEAKER = 1 << 3
CAP_MICROPHONE = 1 << 4
CAP_ASSET_STORAGE = 1 << 5
CAP_AUDIO_STREAM = 1 << 6
CAP_LOCAL_ANIMATION = 1 << 7

MODE_TO_BYTE = {
    "idle": MODE_IDLE,
    "day_work": MODE_DAY_WORK,
    "night_exec": MODE_NIGHT_EXEC,
}

WORK_STATE_TO_BYTE = {
    "stopped": WORK_STOPPED,
    "starting": WORK_STARTING,
    "running": WORK_RUNNING,
    "paused": WORK_PAUSED,
    "completed": WORK_COMPLETED,
    "failed": WORK_FAILED,
}

BYTE_TO_MODE = {v: k for k, v in MODE_TO_BYTE.items()}
BYTE_TO_WORK_STATE = {v: k for k, v in WORK_STATE_TO_BYTE.items()}
